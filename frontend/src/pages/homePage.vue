// 获取要加载的消息
async function loadHistoryMessages(id) {
  console.log("loadHistoryMessages")
  try {
    // 1. 获取必要的参数（确保都为有效数字）
    const messageIndex = store.getters.getMessageIndex;
    const selectedId = store.getters.getSelectedId;
    console.log("getSelectedId", store.getters.getSelectedId);
    console.log("messageIndex", messageIndex)
    // 2. 传入 id 调用 getOldMessageLength
    const assistantMsgLength = store.getters.getOldMessageLength(selectedId);
    console.log("assistantMsgLength", assistantMsgLength);
    const pageScore = store.getters.getPageScore;


    // 4. 正确计算
    const newL = messageIndex + 1 + assistantMsgLength;

    const pages = Math.floor(newL / pageScore) + 1; // 建议用 Math.floor 确保整数
    console.log("newL", newL, "getPageScore", pageScore, "pages", pages);
    const restM = newL % pageScore;
    console.log('restm', restM)
    const messages = await MessageService.findMessagesByPage(
      store.getters.getSelectedId,
      store.getters.getPageScore,
      pages
    );
    console.log("获取的", messages)

    if (Array.isArray(messages)) {

      const filteredMessages = messages.slice(restM);
      console.log("filteredMessages", filteredMessages)
      // 关键修正：只有当原始消息数组长度小于页大小，才认为没有更多数据
      const isLastPage = messages.length < store.getters.getPageScore;

      if (filteredMessages.length > 0) {
        store.commit('addOldMessageHistorybyId', { messages: filteredMessages, id: store.getters.getSelectedId });

        // 只有当前是最后一页时才关闭加载更多
        hasMore.value = !isLastPage;
        return filteredMessages.length;
      } else {
        // 如果原始消息不为空但过滤后为空，可能是计算问题，不应关闭加载
        hasMore.value = !isLastPage && messages.length > 0;
        return 0;
      }
    } else {
      console.error('返回的消息不是数组', messages);
      hasMore.value = false;
      return 0;
    }
  } catch (error) {
    console.error('加载历史消息失败', error);
    // 出错时不直接关闭加载，给用户重试机会
    hasMore.value = true;
    return 0;
  }
}

// 记录当前的位置，并调用loadHistoryMessages，
const loadMoreMessages = async () => {
  try {
    // 记录加载前的滚动状态
    const oldScrollHeight = scrollContainer.value?.scrollHeight || 0;
    const oldScrollTop = scrollContainer.value?.scrollTop || 0;

    // 加载历史消息
    messageHeight.value = messageHeight.value + await loadHistoryMessages(store.getters.getSelectedId);

    // 保持滚动位置
    nextTick(() => {
      if (scrollContainer.value) {
        const heightDiff = scrollContainer.value.scrollHeight - oldScrollHeight;
        scrollContainer.value.scrollTop = oldScrollTop + heightDiff;
      }
    });
  } catch (error) {
    console.error('加载历史消息失败:', error);
    loadingMore.value = false;
  }
};


// 上拉加载的判断条件
const handleScroll = async (e) => {
  if (scrollTimer) clearTimeout(scrollTimer);
  scrollTimer = setTimeout(async () => {
    if (!e.target) return;
    const element = e.target;
    const currentScrollTop = element.scrollTop;

    // 优化滚动方向判断：
    // 1. 有滚动条时，通过 scrollTop 变化判断
    // 2. 无滚动条时，通过滚轮事件的 deltaY 判断（需额外监听 wheel 事件）
    // 在 handleScroll 中调整 isScrollingUp 判断
    let isScrollingUp = currentScrollTop < lastScrollTop.value;
    // 记录上一次 scrollTop
    lastScrollTop.value = currentScrollTop;

    // 触发条件：向上滚动 + 滚动顶部接近顶部 + 不在加载中 + 还有更多数据
    // 移除对滚动条存在的判断（element.scrollHeight > element.clientHeight）
    if (
      isScrollingUp &&
      currentScrollTop < 50 &&
      !loadingMore.value &&
      hasMore.value
    ) {
      loadingMore.value = true;
      await loadMoreMessages();
      loadingMore.value = false;
    }
  }, 100);
};

